package day5;

public class Car {

    int year;
    String colour;
    String model;

    public int getYear(){
        return year;
    }
    public void setYear(int year) {
        this.year = year;
    }

    public String getColour() {
        return colour;
    }
    public void setColour(String colour) {
        this.colour = colour;
    }

    public String getModel() {
        return model;
    }
    public void setModel(String model) {
        this.model = model;
    }
}
